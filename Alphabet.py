import turtle
from random import randint
turtle.ht()
turtle.speed(12)
turtle.bgcolor('#f0ebe1')
turtle.setworldcoordinates(0, 0, 360, 360)
turtle.pencolor('#291101')
turtle.hideturtle()
turtle.pensize(5)
turtle.up()
turtle.goto(330, 90)
turtle.down()
turtle.goto(330, 270)
turtle.up()
def f(x, y):
    turtle.goto(x, y)
    for i in range(10):
        turtle.forward(20)
        turtle.up()
        turtle.forward(10)
        turtle.down()
    turtle.up()
f(30, 240)
f(30, 210)
f(30, 180)
f(30, 150)
f(30, 120)
t1=turtle.Turtle()
t2=turtle.Turtle()
t3=turtle.Turtle()
t4=turtle.Turtle()
def create(name, y):
    name.up()
    name.goto(30, y)
    name.shape('turtle')
    a=['#e93681', '#6327ed', '#c328ed', '#c328ed', '#16e0d4', '#24e80c', '#f79007', '#4207f7']
    s=randint(0, len(a)-1)
    name.color(a[s])
create(t1, 225)
create(t2, 195)
create(t3, 165)
create(t4, 135)
st1=randint(1, 20)
st2=randint(1, 20)
st3=randint(1, 20)
st4=randint(1, 20)
while t1.xcor()<330 and t2.xcor()<330 and t3.xcor()<330 and t4.xcor()<330:
    t1.forward(st1)
    t2.forward(st2)
    t3.forward(st3)
    t4.forward(st4)
turtle.exitonclick()